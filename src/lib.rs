#[near_bindgen]
#[derive(BorshDeserialize, BorshSerialize, PanicOnDefault)]
pub struct Contract {
    tokens: NonFungibleToken,
    metadata: LazyOption<NFTContractMetadata>,
}

#[derive(BorshSerialize, BorshStorageKey)]
enum StorageKey {
    NonFungibleToken,
    Metadata,
    TokenMetadata,
    Enumeration,
    Approval,
}

#[near_bindgen]
impl Contract {
    #[init]
    pub fn new(owner_id: ValidAccountId, metadata: NFTContractMetadata) -> Self {
        assert!(!env::state_exists(), "Already initialized");
        metadata.assert_valid();
        Self {
            tokens: NonFungibleToken::new(
                StorageKey::NonFungibleToken,
                owner_id,
                Some(StorageKey::TokenMetadata),
                Some(StorageKey::Enumeration),
                Some(StorageKey::Approval),
            ),
            metadata: LazyOption::new(StorageKey::Metadata, Some(&metadata)),
        }
    }

    #[payable]
    pub fn nft_mint(
        &mut self,
        token_id: TokenId,
        token_owner_id: ValidAccountId,
        token_metadata: TokenMetadata,
    ) -> Token {
        self.tokens.mint(token_id, token_owner_id, Some(token_metadata))
    }
}


/// 需要override原先的实现，因为需要按照paras的规范修改log
#[near_bindgen]
impl NonFungibleTokenCore for Contract {
    fn nft_token(self, token_id: TokenId) -> Option<Token> {
        self.tokens.nft_token(token_id)
    }

    fn mint(
        &mut self,
        token_id: TokenId, 
        token_owner_id: ValidAccountId, 
        token_metadata: Option<TokenMetadata>
    ) -> Token {
        panic!("please call nft_mint")
    }

    fn nft_transfer(
        &mut self,
        receiver_id: ValidAccountId, 
        token_id: TokenId, 
        approval_id: Option<u64>, 
        memo: Option<String>
    ) {
        self.tokens.nft_transfer(receiver_id.clone(), token_id.clone(), approval_id, memo);
        let owner_id = self.tokens.owner_by_id.get(&token_id).unwrap();
        env::log(
            json!({
                "type": "nft_transfer",
                "params": {
                    "token_id": token_id,
                    "sender_id": owner_id,
                    "receiver_id": receiver_id,
                }
            })
            .to_string()
            .as_bytes()
        );
    }

    fn nft_transfer_call(
        &mut self,
        receiver_id: ValidAccountId, 
        token_id: TokenId, 
        approval_id: Option<u64>, 
        memo: Option<String>, 
        msg: String
    ) -> PromiseOrValue<bool> {
        self.tokens.nft_transfer_call(receiver_id, token_id, approval_id, memo, msg)
    }
}

impl NonFungibleTokenResolver for Contract {
    fn nft_resolve_transfer(
        &mut self,
        previous_owner_id: AccountId, 
        receiver_id: AccountId, 
        token_id: TokenId, 
        approvals: Option<HashMap<AccountId, u64>>
    ) -> bool {
        let transferred = self.tokens.nft_resolve_transfer(
            previous_owner_id.clone(),
            receiver_id.clone(),
            token_id.clone(),
            approvals,
        );

        if transferred {
            env::log(
                json!({
                    "type": "nft_transfer",
                    "params": {
                        "token_id": token_id,
                        "sender_id": previous_owner_id,
                        "receiver_id": receiver_id,
                    }
                })
                .to_string()
                .as_bytes()
            );
        };

        return transferred;
    }
}

near_contract_standards::impl_non_fungible_token_approval!(Contract, tokens);
